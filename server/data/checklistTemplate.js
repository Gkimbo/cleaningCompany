/**
 * Convert template data to editor format
 * @returns {Object} Checklist data in the format expected by the editor
 */
const getEditorFormat = () => {
  const sections = checklistTemplate.map((section, sectionIndex) => ({
    id: `template-section-${sectionIndex}`,
    title: section.title,
    icon: section.icon,
    displayOrder: section.displayOrder - 1, // Editor uses 0-based
    items: section.items.map((item, itemIndex) => ({
      id: `template-item-${sectionIndex}-${itemIndex}`,
      content: item.content,
      proTip: item.proTip || null,
      displayOrder: item.displayOrder - 1, // Editor uses 0-based
      indentLevel: 0,
      formatting: { bold: false, italic: false, bulletStyle: "disc" },
      children: [],
    })),
  }));

  return {
    sections,
    metadata: {
      lastModified: new Date().toISOString(),
      isTemplate: true,
    },
  };
};

/**
 * Get template statistics
 * @returns {Object} Stats about the template
 */
const getTemplateStats = () => {
  const totalItems = checklistTemplate.reduce(
    (sum, section) => sum + section.items.length,
    0
  );
  const proTipCount = checklistTemplate.reduce(
    (sum, section) =>
      sum + section.items.filter((item) => item.proTip).length,
    0
  );

  return {
    sectionCount: checklistTemplate.length,
    itemCount: totalItems,
    proTipCount,
  };
};

module.exports = {
  checklistTemplate,
  getEditorFormat,
  getTemplateStats,
};
